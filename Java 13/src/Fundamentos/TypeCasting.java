package m1Fundamentos;

public class TypeCasting {

	public static void main(String[] args) {
		/*
		 *Conversion of types for others types 
		 *Declare the type, name,
		 * in "()" enter the type that should be considered
		 */
		
		//"a" receive a value, but, then must the value how "float" 
		float a = (float) 1.8;
		System.out.println(a);
		int b = (int) 1.8;
		System.out.println(b);
		
	}
}
